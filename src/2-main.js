// Data Type, Variable, Assignment
// three keywords; var, let, const
let username = "rohit";
let counter = 100;
let price = 99.99;

console.log(username);
console.log(counter)
console.log(price);

// let allows us to change the value
counter = 200;
console.log(200);


// Variable with const keyword
const totalAmount = 500;
// totalAmount = 600;
console.log(totalAmount);
